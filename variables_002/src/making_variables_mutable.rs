fn main() {
    let mut age = 46;
    let mut name = "Timothy Unkert";
    println!("My name is {name} and I am {age} years old!");
    age = 47;
    name = "Sean";
    println!("My name is {name} and I am {age} years old!");
    /*
    This works because I added the mut keyword before the variable name
     */
}